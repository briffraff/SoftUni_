input = input()

if (input == "banana" or
        input == "apple" or
        input == "kiwi" or
        input == "cherry" or
        input == "lemon" or
        input == "grapes"):
    print("fruit")
elif (input == "tomato" or
      input == "cucumber" or
      input == "pepper" or
      input == "carrot"):
    print("vegetable")
else:
    print("unknown")
